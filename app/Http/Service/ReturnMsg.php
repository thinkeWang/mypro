        '1020'      =>  '属性不存在',
        /*分页规则**/
        '-1003'      =>  '分页数据有错误！',

    ];


    /**
     *获取标准提示信息
     */
    public static function getMsg($code, $others = []){
        $error = [
            'code' => $code,
            'msg' => self::$msgMap[$code]
        ];

        if(is_array($others) && !empty($others)){
            $error = array_merge($error, $others);
        }
        return $error;
    }


}
